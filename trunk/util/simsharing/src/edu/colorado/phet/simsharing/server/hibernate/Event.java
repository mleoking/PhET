package edu.colorado.phet.simsharing.server.hibernate;

import java.util.Date;

/**
 * @author Sam Reid
 */
public class Event {
    private long id;
    private String name;
    private Date date;

    public Event() {
    }

    public Event( String name, Date date ) {
        this.name = name;
        this.date = date;
    }

    public long getId() {
        return id;
    }

    public void setId( long id ) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public Date getDate() {
        return date;
    }

    public void setName( String name ) {
        this.name = name;
    }

    public void setDate( Date date ) {
        this.date = date;
    }
}
